<?php
// Initialize a variable
$x = 10;

// Do something with the x's value
echo $x + 10;
echo '<br/>';

// Concatenate x's value with a string using the dot (period)
echo 'The value of x is '.$x;
echo '<br/>';

// Assign x a new value, 27 less than its current value
$x = $x - 27;

// Variables inside of *double* quotes are evaluated
echo "The value of x is $x";
?>